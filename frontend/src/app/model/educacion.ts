export class Educacion {
    id : number;
    nombreE : string;
    descripcionE : string;
    iconoE: string;
    anioE: number;
    webE: string;
     
    constructor(id : number, nombreE: string, descripcionE : string, iconoE : string, anioE : number, webE : string){
        this.id = id;
        this.nombreE = nombreE;
        this.descripcionE = descripcionE;
        this.iconoE = iconoE;
        this.anioE = anioE;
        this.webE = webE;


    }
}
